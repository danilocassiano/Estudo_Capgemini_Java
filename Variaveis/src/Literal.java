public class Literal {
    public static void main(String[] args) throws Exception {

        int numero;
        String caracteres;
        char caracter;
        boolean falsoOuVerdadeiro;

        numero = 10;
        caracteres = "Meu Curso de Java";
        caracter = 'D';
        falsoOuVerdadeiro = true;



        System.out.println("Meu numero é: " + numero);
        System.out.println("Meu Caracter é: " + caracteres);
        System.out.println("Meus Caracteres é: " + caracter);
        System.out.println("Meus verdadeiro é: " + falsoOuVerdadeiro);
    }
}
